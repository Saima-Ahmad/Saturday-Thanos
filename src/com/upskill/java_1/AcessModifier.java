package com.upskill.java_1;

public class AcessModifier {
	
	// Different Access Modifier in Java
	
	public String name = "Saima";    //Public - Any class access
	
	private int age = 55;           //Private- only same class can access
	
	protected int ssn = 12354684;   //Protected - classes in the same package and subclass can access
	
	    String address= "heritage Tower";   // default - class in the same package can access
			

}
